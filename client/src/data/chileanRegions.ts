// Helper function to get all regions
export const getAllRegions = () => Object.keys(chileanRegions);

// Helper function to get communes for a specific region
export const getCommunesByRegion = (region: string) => chileanRegions[region as keyof typeof chileanRegions] || [];

// Helper function to check if a region exists
export const isValidRegion = (region: string) => region in chileanRegions;

// Helper function to check if a commune exists in a region
export const isValidCommune = (region: string, commune: string) => {
  const communes = getCommunesByRegion(region);
  return communes.includes(commune);
};